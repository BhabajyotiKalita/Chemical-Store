void getCustomerid()
{

    try
    {
        PreparedStatement PS=con.prepareStatement("select * from product_order where orderno='" + jComboBox1.getSelectedItem() +"'");
        ResultSet rs=PS.executeQuery();
        if(rs.next())
        {
            txtcustomername.setText(rs.getString("C_id"));
            txtqnty.setText(rs.getString("qnty"));
            txtrate.setText(rs.getString("rate"));
            txtamount.setText(rs.getString("total"));
            
        }


    }
    catch(Exception e)
    {
        JOptionPane.showMessageDialog(new JPanel(),e,"Error 111",1);

    }
}
