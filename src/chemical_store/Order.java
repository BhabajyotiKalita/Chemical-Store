public Vector getcustomerid()
    {

        Vector<String> data = new Vector<String>();


        try
        {
        PreparedStatement pre = con.prepareStatement("select C_id from customer");
        ResultSet rs = pre.executeQuery();

        while(rs.next())
        {
              data.add(rs.getString(1));
        }
        }
        catch(Exception e)
        {
           JOptionPane.showMessageDialog(new JPanel(),e,"Error 112",2);
        }
        return data;

    }
int getNextorderno()
{
    int B_no=1;

    try
    {
        PreparedStatement PS=con.prepareStatement("select max(B_no) from bill");
        ResultSet rs=PS.executeQuery();
        if(rs.next())
        {
            B_no=Integer.parseInt(rs.getString(1)) +1;

        }
        else
        {
            B_no=1;
        }
    }
    catch(Exception e)
    {
        B_no=1;
    }

    return B_no;

}
public Vector getproductid()
    {

        Vector<String> data = new Vector<String>();


        try
        {
        PreparedStatement pre = con.prepareStatement("select p_id from product");
        ResultSet rs = pre.executeQuery();

        while(rs.next())
        {
              data.add(rs.getString(1));
        }
        }
        catch(Exception e)
        {
           JOptionPane.showMessageDialog(new JPanel(),e,"Error 112",2);
        }
        return data;

    }

