void saveData()
{
    PreparedStatement ps=null;

    try
    {
        ps=con.prepareStatement("insert into product_order values(?,?,?,?,?,?,?,?)");
        ps.setString(1, txtorderno.getText());
        ps.setString(2,(String)jComboBox1.getSelectedItem());
        ps.setString(3,(String)cmbcustomerid.getSelectedItem());
        ps.setString(4,txtorderdate.getText());
        ps.setString(5,txtaddress.getText());
        ps.setString(6,txtqnty.getText());
         ps.setString(7,txtcost.getText());
          ps.setString(8,txttotal.getText());
       
       
        ps.executeUpdate();
        JOptionPane.showMessageDialog(new JPanel(),"Saved","Done",1);
    }
    catch(Exception e)
    {
        JOptionPane.showMessageDialog(new JPanel(),e,"Error 106",1);
    }

}
