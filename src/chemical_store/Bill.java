void saveData()
{
    PreparedStatement ps=null;

    try
    {
        ps=con.prepareStatement("insert into Bill values(?,?,?,?,?,?,?,?,?,?)");
        ps.setString(1, txtbillno.getText());
        ps.setString(2,(String)jComboBox1.getSelectedItem());
        ps.setString(3,txtcustomername.getText());
        ps.setString(4,txtbilldate.getText());
        ps.setString(5,txtbilltime.getText());
        ps.setString(6,txtqnty.getText());
        ps.setString(7,txtrate.getText());
        ps.setString(8,txtamount.getText());
        ps.setString(9,txtvat.getText());
        ps.setString(10,txtnet.getText());

        ps.executeUpdate();
        JOptionPane.showMessageDialog(new JPanel(),"Saved","Done",1);
    }
    catch(Exception e)
    {
        JOptionPane.showMessageDialog(new JPanel(),e,"Error 106",1);
    }

}
